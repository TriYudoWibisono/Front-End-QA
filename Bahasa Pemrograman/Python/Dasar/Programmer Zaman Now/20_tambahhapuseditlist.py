# Belajar Edit List 

nama = ["Bogi", "Judin", "Frans"]
print(nama)

# jika mau nambah data nama

nama.append("Aji")
print(nama)

# jika mau hapus data nama

nama.remove("Judin")
print(nama)

# jika mau akses data nama ke 1 / index 0

print(nama[0])

# jika mau Edit/Ubah data nama 

nama[0] = "Wicaksana"
print(nama)



