# Belajar Numeric Types

# secara detail pada python itu ada 3 jenis type data numeric. yaitu integer, float dan complex.
# integer : numeric biasa tanpa decimal (tidak bisa ada koma/titik)
# float : angka ada decimalnya seperti 1.5 dll
# complex : jarang di pakai. biasanya di pake untuk matematika

angka  = 1      # bilangan integer
angka2 = 1.5    # bilangan float
total  = angka + angka2     # penjumlahan bilangan integer dan float

# hasilnya 2.5 (otomatis berubah jadi float)
# python akan secara otomatis menkonvensikan menjadi float

print(total)