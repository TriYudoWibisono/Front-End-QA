# Belajar For Loop

# variabel pelanggan tipe datanya adalah list
pelanggan = ["eko", "budi", "joko", "andi"]

# mengakses semua nama pelanggan (cara ribet)
print(pelanggan[0])
print(pelanggan[1])
print(pelanggan[2])
print(pelanggan[3])

# mengakses semua nama pelanggan (cara simpel)
for nama in pelanggan :
    print(nama)

# variabel nama digunakan untuk menyimpan hasil list dari looping

print("Setelah di tambah")

pelanggan.append("judin")

for nama1 in pelanggan :
    print(nama1)

