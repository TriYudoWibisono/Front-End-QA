# Operator Assignmnet adalah operasi yang bisa di lakukan dengan penyingkatan

a = 5 # ini adalah assignment
print('nilai a = ',a)

a += 1 # artinya a = a + 1
print('nilai a += 1, a menjadi ',a)

a -= 2 # artinya a = a - 1
print('nilai a -= 1, a menjadi ',a)

a *= 5 # artinya a = a x 5
print('nilai a *= 1, a menjadi ',a)